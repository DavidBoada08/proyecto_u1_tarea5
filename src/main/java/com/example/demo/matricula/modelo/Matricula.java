package com.example.demo.matricula.modelo;

public class Matricula {
	
	private  String numero;
	private String tipo;
	private String alumnoCod;
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getAlumnoCod() {
		return alumnoCod;
	}
	public void setAlumnoCod(String alumnoCod) {
		this.alumnoCod = alumnoCod;
	}
	

}
